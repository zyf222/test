package tranport;

public class packageinfo {
    private double weight;       //行李的重量
    private double x;             //行李的长
    private double y;             //行李的宽
    private double z;             //行李的高

    public double getWeight() {
        return weight;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void setZ(double z) {
        this.z = z;
    }
}
